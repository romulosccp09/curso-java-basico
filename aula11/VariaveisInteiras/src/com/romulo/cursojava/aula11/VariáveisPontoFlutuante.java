package com.romulo.cursojava.aula11;

public class VariáveisPontoFlutuante {
    
    public static void main(String[] args) {
        
        double valorPassagem = 3.40;
        
        float valorTomate = 3.95f;
        
        double d1 = 123.4;
        
        //mesmo valor em notação científica.
        double d2 = 1.234e2;
        float f1 = 123.4f;
        
        
        System.out.println("Valor da passagem = " + valorPassagem);
        
        System.out.println("Valor do tomate = " + valorTomate);
        
        System.out.println(d1 + " " + d2 + " " + f1);
        
    }
    
}
